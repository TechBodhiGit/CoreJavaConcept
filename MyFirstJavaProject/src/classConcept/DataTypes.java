package classConcept;

public class DataTypes {

	public static void main(String[] args) {
		
		
	byte byteVariable = 10;
	System.out.println("The Value of byteVariable is --- " + byteVariable );
		
		
		
	short shortVariable = 1;
	System.out.println("The Value of shortVariable is --- " + shortVariable );
		
	
	int integerVariable = 102;
	System.out.println("The Value of integerVariable is --- " + integerVariable );
	
	
	long	longVariabe = 9163783063l;
	System.out.println("The Value of longVariabe is --- " + longVariabe );
		
	boolean booleanVariableTRUE = true;
	boolean booleanVariableFalse = false;
	
	System.out.println(booleanVariableTRUE);
	System.out.println(booleanVariableFalse);
	
	char charVariable = 'A';
	System.out.println("The Value of charVariable is --- " + charVariable);
	
	float floatVariable = 2.33f;
	
	System.out.println("The Value of floatVariable is --- " + floatVariable);
		
		
	String courseName = "Selenium";	
	
	System.out.println("The String Value is " + courseName);
		
		
		
		
		
	}

}
