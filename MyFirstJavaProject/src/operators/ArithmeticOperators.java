package operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int a = 105;
		int b = 20;
		
		System.out.println("Addition Opertor :" + (a+b)); //Addition
		
		System.out.println("Subtraction Operator : " + (a-b)); //Subtraction
		
		System.out.println("Multiplication Operator : " + (a*b)); //Multiplication 
		
		System.out.println("Division Operator : " + (a/b)); //Division 
		
		System.out.println("Modulo Operator : " + (a%b)); //Modulo (It returns the remainder after division)
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
